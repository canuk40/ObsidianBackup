/** Structured user identity extracted from a validated SAML assertion. */
data class SamlUserInfo(
    val nameId: String,
    val email: String,
    val roles: List<String>,
    val attributes: Map<String, List<Any>>
)

/** Thrown when SAML assertion validation fails or required attributes are missing. */
class SamlAuthException(message: String, cause: Throwable? = null) : RuntimeException(message, cause)
